### Variable 1: Population, total - Iceland

# Total population is based on the de facto definition of population,
# which counts all residents regardless of legal status or citizenship.
# The values shown are midyear estimates.
row_name_pt <- "Población Total"

### Variable 2: Population growth (annual %) - Iceland

# Annual population growth rate for year t is the exponential
# rate of growth of midyear population from year t-1 to t,
# expressed as a percentage . Population is based on the de
# facto definition of population, which counts all residents
# regardless of legal status or citizenship.
row_name_gpt <- "Crecimiento Poblacional (%)"

### Variable 3: Life expectancy at birth, total (years) - Iceland

# Life expectancy at birth indicates the number of years a newborn
# infant would live if prevailing patterns of mortality at the time
# of its birth were to stay the same throughout its life.
row_name_le <- "Esperanza de Vida (años)"